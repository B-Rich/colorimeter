static void fir(double *buf, double *coeff, unsigned int n)
{
	unsigned int i, j;
	double f;

	n -= FIR_SIZE;

	for (i = 0; i < n; i++) {
		f = 0.0;
		for (j = 0; j < FIR_SIZE; j++)
			f += buf[i + j] * coeff[j];
		buf[i] = f;
	}
}


int capture_data(struct iio_device *dev, unsigned int n,
	double *a1, double *a2)
{
	struct iio_buffer *buf;
	unsigned int i;
	uint32_t *data;
	double *ch1, *ch2;
	double *phase1, *phase2;
	double *i1, *q1, *i2, *q2;
	double _a1, _a2;
	double *dbuf;

	buf = iio_device_create_buffer(dev, n, false);
	if (!buf)
		return -errno;

	dbuf = malloc(sizeof(*dbuf) * n * 8);
	ch1 = dbuf;
	ch2 = dbuf + n;
	phase1 = dbuf + 2 * n;
	phase2 = dbuf + 3 * n;
	i1 = dbuf + 4 * n;
	q1 = dbuf + 5 * n;
	i2 = dbuf + 6 * n;
	q2 = dbuf + 7 * n;

	iio_buffer_refill(buf);

	data = iio_buffer_start(buf);
	for (i = 0; i < n; i++) {
		ch1[i] = (double)data[i*4] - (1<<23);
		ch2[i] = (double)data[i*4 + 2] - (1<<23);
		phase1[i] = ((double)data[i*4 + 1]) * M_PI / (1<<31);
		phase2[i] = ((double)data[i*4 + 3]) * M_PI / (1<<31);
	}

	fir(ch1, hpf_filter, n);
	fir(ch2, hpf_filter, n);

	n -= FIR_SIZE;

	for (i = 0; i < n; i++) {
		i1[i] = cos(phase1[i]) * ch1[i];
		q1[i] = sin(phase1[i]) * ch1[i];
		i2[i] = cos(phase2[i]) * ch2[i];
		q2[i] = sin(phase2[i]) * ch2[i];
	}

	fir(i1, lpf_filter, n);
	fir(q1, lpf_filter, n);
	fir(i2, lpf_filter, n);
	fir(q2, lpf_filter, n);

	n -= FIR_SIZE;

	_a1 = 0.0;
	_a2 = 0.0;

	for (i = 0; i < n; i++) {
		_a1 += sqrt(i1[i]*i1[i] + q1[i]*q1[i]);
		_a2 += sqrt(i2[i]*i2[i] + q2[i]*q2[i]);
	}

	*a1 = _a1 / n;
	*a2 = _a2 / n;

	free(dbuf);
	iio_buffer_destroy(buf);

	return 0;
}
